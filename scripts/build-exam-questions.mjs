import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';
import { bankToExamMeta, loadAllQuestionBanks } from './lib/question-sources.mjs';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, '..');

const JS_OUTPUTS = {
  'ai-102': {
    exportName: 'ai102Questions',
    metaName: 'ai102ExamMeta',
    output: 'apps/web/src/data/ai102Questions.js',
  },
};

function writeModule(examCode, certId, questions, meta) {
  const cfg = JS_OUTPUTS[certId];
  if (!cfg) return null;

  const outPath = path.join(root, cfg.output);
  fs.mkdirSync(path.dirname(outPath), { recursive: true });

  const body = `// Generated by scripts/build-exam-questions.mjs (from apps/web/src/data bundles)
// ${examCode}: ${questions.length} questions (${questions.filter((q) => q.quizEligible).length} quiz-eligible MC)

export const ${cfg.exportName} = ${JSON.stringify(questions, null, 2)};

export const ${cfg.metaName} = ${JSON.stringify(meta, null, 2)};
`;

  fs.writeFileSync(outPath, body, 'utf8');
  return outPath;
}

const banks = await loadAllQuestionBanks();

for (const bank of banks) {
  const { cert, questions } = bank;
  const meta = bankToExamMeta(bank);
  const outPath = writeModule(cert.examCode, cert.id, questions, meta);
  if (outPath) {
    console.log(
      `${cert.examCode}: wrote ${questions.length} questions (${meta.quizEligible} MC) → ${path.relative(root, outPath)}`,
    );
  } else {
    console.log(`${cert.examCode}: ${questions.length} questions — DB via migrate script`);
  }
}
